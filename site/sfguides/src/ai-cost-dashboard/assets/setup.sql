CREATE OR REPLACE PROCEDURE REFRESH_CORTEX_AI_COSTS(DAYS_BACK INT DEFAULT 7)
RETURNS VARIANT
LANGUAGE SQL
AS
'
DECLARE
    result OBJECT DEFAULT OBJECT_CONSTRUCT();
    rows_affected INT;
    region_type VARCHAR;
    cross_region_value VARCHAR;
BEGIN
    SHOW PARAMETERS LIKE ''CORTEX_ENABLED_CROSS_REGION'' IN ACCOUNT;
    LET rs RESULTSET := (SELECT "value" AS VAL FROM TABLE(RESULT_SCAN(LAST_QUERY_ID())));
    LET cur CURSOR FOR rs;
    cross_region_value := ''DISABLED'';
    FOR row_var IN cur DO
        cross_region_value := row_var.VAL;
    END FOR;
    IF (cross_region_value IN (''ANY_REGION'', ''AWS_GLOBAL'', ''GCP_GLOBAL'', ''AZURE_GLOBAL'')) THEN
        region_type := ''GLOBAL'';
    ELSE
        region_type := ''REGIONAL'';
    END IF;

    -- 1. CORTEX_AISQL_USAGE (incremental by QUERY_ID)
    MERGE INTO CORTEX_AISQL_USAGE tgt
    USING (
        SELECT MAX(USAGE_TIME) AS USAGE_TIME, MAX(MODEL_NAME) AS MODEL_NAME, MAX(FUNCTION_NAME) AS FUNCTION_NAME, SUM(TOKEN_CREDITS) AS TOKEN_CREDITS, SUM(TOKENS) AS TOKENS, QUERY_ID, MAX(QUERY_TAG) AS QUERY_TAG, MAX(USER_ID) AS USER_ID, MAX(WAREHOUSE_ID) AS WAREHOUSE_ID,
               SUM(COALESCE(TOKENS_GRANULAR:input, 0)::NUMBER) AS INPUT_TOKENS,
               SUM(COALESCE(TOKENS_GRANULAR:output, 0)::NUMBER) AS OUTPUT_TOKENS,
               SUM(COALESCE(TOKENS_GRANULAR:cache_read_input, 0)::NUMBER) AS CACHE_READ_TOKENS,
               SUM(COALESCE(TOKENS_GRANULAR:cache_write_input, 0)::NUMBER) AS CACHE_WRITE_TOKENS
        FROM SNOWFLAKE.ACCOUNT_USAGE.CORTEX_AISQL_USAGE_HISTORY
        WHERE USAGE_TIME >= DATEADD(''day'', -:DAYS_BACK, CURRENT_TIMESTAMP())
        GROUP BY QUERY_ID
    ) src ON tgt.QUERY_ID = src.QUERY_ID
    WHEN MATCHED THEN UPDATE SET tgt.TOKEN_CREDITS = src.TOKEN_CREDITS, tgt.TOKENS = src.TOKENS, tgt.INPUT_TOKENS = src.INPUT_TOKENS, tgt.OUTPUT_TOKENS = src.OUTPUT_TOKENS, tgt.CACHE_READ_TOKENS = src.CACHE_READ_TOKENS, tgt.CACHE_WRITE_TOKENS = src.CACHE_WRITE_TOKENS, tgt.LAST_REFRESHED = CURRENT_TIMESTAMP()
    WHEN NOT MATCHED THEN INSERT (USAGE_TIME, MODEL_NAME, FUNCTION_NAME, TOKEN_CREDITS, TOKENS, QUERY_ID, QUERY_TAG, USER_ID, WAREHOUSE_ID, INPUT_TOKENS, OUTPUT_TOKENS, CACHE_READ_TOKENS, CACHE_WRITE_TOKENS)
        VALUES (src.USAGE_TIME, src.MODEL_NAME, src.FUNCTION_NAME, src.TOKEN_CREDITS, src.TOKENS, src.QUERY_ID, src.QUERY_TAG, src.USER_ID, src.WAREHOUSE_ID, src.INPUT_TOKENS, src.OUTPUT_TOKENS, src.CACHE_READ_TOKENS, src.CACHE_WRITE_TOKENS);
    rows_affected := SQLROWCOUNT;
    result := OBJECT_INSERT(result, ''CORTEX_AISQL_USAGE'', rows_affected);

    -- 2. CORTEX_ANALYST_USAGE (incremental by START_TIME, USERNAME)
    MERGE INTO CORTEX_ANALYST_USAGE tgt
    USING (
        SELECT START_TIME, END_TIME, COALESCE(USERNAME, ''SYSTEM'') AS USERNAME, CREDITS, REQUEST_COUNT
        FROM SNOWFLAKE.ACCOUNT_USAGE.CORTEX_ANALYST_USAGE_HISTORY
        WHERE START_TIME >= DATEADD(''day'', -:DAYS_BACK, CURRENT_TIMESTAMP())
    ) src ON tgt.START_TIME = src.START_TIME AND tgt.USERNAME = src.USERNAME
    WHEN MATCHED THEN UPDATE SET tgt.END_TIME = src.END_TIME, tgt.CREDITS = src.CREDITS, tgt.REQUEST_COUNT = src.REQUEST_COUNT, tgt.LAST_REFRESHED = CURRENT_TIMESTAMP()
    WHEN NOT MATCHED THEN INSERT (START_TIME, END_TIME, USERNAME, CREDITS, REQUEST_COUNT)
        VALUES (src.START_TIME, src.END_TIME, src.USERNAME, src.CREDITS, src.REQUEST_COUNT);
    rows_affected := SQLROWCOUNT;
    result := OBJECT_INSERT(result, ''CORTEX_ANALYST_USAGE'', rows_affected);

    -- 3. CORTEX_DOCUMENT_PROCESSING_USAGE (incremental by QUERY_ID)
    MERGE INTO CORTEX_DOCUMENT_PROCESSING_USAGE tgt
    USING (
        SELECT QUERY_ID, CREDITS_USED, START_TIME, END_TIME, FUNCTION_NAME, MODEL_NAME, OPERATION_NAME, PAGE_COUNT, DOCUMENT_COUNT, FEATURE_COUNT
        FROM SNOWFLAKE.ACCOUNT_USAGE.CORTEX_DOCUMENT_PROCESSING_USAGE_HISTORY
        WHERE START_TIME >= DATEADD(''day'', -:DAYS_BACK, CURRENT_TIMESTAMP())
    ) src ON tgt.QUERY_ID = src.QUERY_ID
    WHEN NOT MATCHED THEN INSERT (QUERY_ID, CREDITS_USED, START_TIME, END_TIME, FUNCTION_NAME, MODEL_NAME, OPERATION_NAME, PAGE_COUNT, DOCUMENT_COUNT, FEATURE_COUNT)
        VALUES (src.QUERY_ID, src.CREDITS_USED, src.START_TIME, src.END_TIME, src.FUNCTION_NAME, src.MODEL_NAME, src.OPERATION_NAME, src.PAGE_COUNT, src.DOCUMENT_COUNT, src.FEATURE_COUNT);
    rows_affected := SQLROWCOUNT;
    result := OBJECT_INSERT(result, ''CORTEX_DOCUMENT_PROCESSING_USAGE'', rows_affected);

    -- 4. CORTEX_FINE_TUNING_USAGE (incremental by START_TIME, MODEL_NAME)
    MERGE INTO CORTEX_FINE_TUNING_USAGE tgt
    USING (
        SELECT START_TIME, END_TIME, MODEL_NAME, TOKEN_CREDITS, TOKENS
        FROM SNOWFLAKE.ACCOUNT_USAGE.CORTEX_FINE_TUNING_USAGE_HISTORY
        WHERE START_TIME >= DATEADD(''day'', -:DAYS_BACK, CURRENT_TIMESTAMP())
    ) src ON tgt.START_TIME = src.START_TIME AND COALESCE(tgt.MODEL_NAME, '''') = COALESCE(src.MODEL_NAME, '''')
    WHEN MATCHED THEN UPDATE SET tgt.END_TIME = src.END_TIME, tgt.TOKEN_CREDITS = src.TOKEN_CREDITS, tgt.TOKENS = src.TOKENS, tgt.LAST_REFRESHED = CURRENT_TIMESTAMP()
    WHEN NOT MATCHED THEN INSERT (START_TIME, END_TIME, MODEL_NAME, TOKEN_CREDITS, TOKENS)
        VALUES (src.START_TIME, src.END_TIME, src.MODEL_NAME, src.TOKEN_CREDITS, src.TOKENS);
    rows_affected := SQLROWCOUNT;
    result := OBJECT_INSERT(result, ''CORTEX_FINE_TUNING_USAGE'', rows_affected);

    -- 5. CORTEX_FUNCTIONS_QUERY_USAGE (incremental by QUERY_ID)
    MERGE INTO CORTEX_FUNCTIONS_QUERY_USAGE tgt
    USING (
        SELECT f.QUERY_ID, f.WAREHOUSE_ID, f.MODEL_NAME, f.FUNCTION_NAME, f.TOKENS, f.TOKEN_CREDITS, q.START_TIME
        FROM SNOWFLAKE.ACCOUNT_USAGE.CORTEX_FUNCTIONS_QUERY_USAGE_HISTORY f
        JOIN SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY q ON f.QUERY_ID = q.QUERY_ID
        WHERE q.START_TIME >= DATEADD(''day'', -:DAYS_BACK, CURRENT_TIMESTAMP())
    ) src ON tgt.QUERY_ID = src.QUERY_ID
    WHEN NOT MATCHED THEN INSERT (QUERY_ID, WAREHOUSE_ID, MODEL_NAME, FUNCTION_NAME, TOKENS, TOKEN_CREDITS, START_TIME)
        VALUES (src.QUERY_ID, src.WAREHOUSE_ID, src.MODEL_NAME, src.FUNCTION_NAME, src.TOKENS, src.TOKEN_CREDITS, src.START_TIME);
    rows_affected := SQLROWCOUNT;
    result := OBJECT_INSERT(result, ''CORTEX_FUNCTIONS_QUERY_USAGE'', rows_affected);

    -- 7. CORTEX_PROVISIONED_THROUGHPUT_USAGE (incremental by PROVISIONED_THROUGHPUT_ID, INTERVAL_START_TIME)
    MERGE INTO CORTEX_PROVISIONED_THROUGHPUT_USAGE tgt
    USING (
        SELECT PROVISIONED_THROUGHPUT_ID, AI_SERVICE, INTERVAL_START_TIME, INTERVAL_END_TIME, CLOUD_SERVICE_PROVIDER, MODEL_NAME, TERM_START_DATE, TERM_END_DATE, PTU_COUNT, PTU_CREDITS
        FROM SNOWFLAKE.ACCOUNT_USAGE.CORTEX_PROVISIONED_THROUGHPUT_USAGE_HISTORY
        WHERE INTERVAL_START_TIME >= DATEADD(''day'', -:DAYS_BACK, CURRENT_TIMESTAMP())
