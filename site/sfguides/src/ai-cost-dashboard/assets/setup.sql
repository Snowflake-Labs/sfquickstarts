DECLARE
    rows_affected INT;
    region_type VARCHAR;
    cross_region_value VARCHAR;
BEGIN
    SHOW PARAMETERS LIKE ''CORTEX_ENABLED_CROSS_REGION'' IN ACCOUNT;
    LET rs RESULTSET := (SELECT "value" AS VAL FROM TABLE(RESULT_SCAN(LAST_QUERY_ID())));
    LET cur CURSOR FOR rs;
    cross_region_value := ''DISABLED'';
    FOR row_var IN cur DO
        cross_region_value := row_var.VAL;
    END FOR;
    IF (cross_region_value IN (''ANY_REGION'', ''AWS_GLOBAL'', ''GCP_GLOBAL'', ''AZURE_GLOBAL'')) THEN
        region_type := ''GLOBAL'';
    ELSE
        region_type := ''REGIONAL'';
